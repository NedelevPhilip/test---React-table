    async getResource (page, amount) {
        return new Promise((resolve) => {
            const index = page === 1 ? 0 : (page - 1) * amount + 1;
            const pagesAmount = Math.ceil(this.data.length / amount);
            setTimeout(() => {
                resolve({
                    users: this.data.slice(index, index + amount),
                    pagination: {
                        pages: pagesAmount,
                        page: page,
                        tableSize: amount
                    }
                })
            }, 500)
        })
    }

    async getUsersByPage(page, amount) {
        return await this.getResource(page, amount)
    }

